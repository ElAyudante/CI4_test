                <h3 class="text-uppercase fw-bold">Introducción</h3>
                <div class="fw-normal fs-6">
                    <p>El principio de responsabilidad proactiva o “accountability” (art. 24 RGPD) de las empresas es uno de los
                    aspectos novedosos y esenciales del REGLAMENTO (UE) 2016/679 DEL PARLAMENTO EUROPEO Y
                    DEL CONSEJO de 27 de abril de 2016 relativo a la protección de las personas físicas en lo que respecta al
                    tratamiento de datos personales y a la libre circulación de estos datos y por el que se deroga la Directiva
                    95/46/CE (Reglamento general de protección de datos), en adelante RGPD y se basa en el reconocimiento
                    de responsabilidad y la prevención de las organizaciones que tratan datos personales. En este sentido, el
                    RGPD considera que actuar sólo cuando ya se ha producido una infracción es insuficiente como estrategia,
                    debido a que dicha infracción puede causar daños de difícil compensación o reparación a los interesados.</p>

                    <p>Así, las organizaciones son responsables de implementar los medios que les permitan realizar un correcto
                    tratamiento de datos personales de forma segura, y además deben ser capaces de demostrar que han
                    actuado con la diligencia debida (art. 5.2 RGPD que implica la inversión de la carga de la prueba). Por tanto,
                    El responsable del tratamiento deberá disponer de documentación que acredite la diligencia en el
                    tratamiento de datos, y el cumplimiento de la normativa.</p>

                    <p>El RGPD dispone en su Considerando 82 que “para demostrar la conformidad y cumplimiento del mismo,
                    tanto el responsable como el encargado de tratamiento deben mantener registros de las actividades de
                    tratamiento bajo su responsabilidad”.</p>

                    <p>Tal y como establece el Considerando 89 del RGPD, se suprime la obligación formal del registro de ficheros
                    de las empresas ante la autoridad de control (en nuestro caso la notificación de ficheros ante el RGPD de la
                    AEPD). Sin embargo, el régimen de inscripción se sustituye por una obligación mucho más amplia de
                    documentación de todos los tratamientos de datos realizados (artículo 30 RGPD)</p>
                </div>
            </li>

            <h4 class="text-uppercase fw-bold">Objeto de este documento</h4>
            <div class="fw-normal fs-6">
                <p>El objeto del presente documento es establecer el registro de las actividades de tratamiento de COLEGIO
                PROFESIONAL DE LOGOPEDAS DE CANTABRIA a efectos de cumplir con lo establecido en el art. 30 RGPD.</p>
                <p>Esta obligación del art. 30 RGPD es aplicable empresas con una plantilla superior a 250 trabajadores
                (Art.30.5 RGPD), o teniendo menos de 250 trabajadores realicen tratamientos que puedan entrañar un
                riesgo para los derechos y libertades fundamentales de los interesados, no sea ocasional o incluya
                categorías especiales de datos incluidos en el artículo 9.1 del RGPD o datos de infracciones o condenas
                penales recogidos en el artículo 10.</p>
                <p>No obstante, el enfoque basado en el riesgo es uno de los pilares del RGPD, apareciendo en reiteradas
                ocasiones a lo largo de su articulado: art 25 relativo a la privacidad por diseño y por defecto, art 32 relativo a
                seguridad, art 33 y 34 relativo a la notificación de brechas de seguridad, artículo 35 relativo a las
                evaluaciones de impacto en protección de datos, artículo 36 relativo a la consulta previa a la autoridad de
                control y articulo 37 relativo al delegado de protección de datos. Este enfoque basado en el riesgo requiere
                con carácter previo identificar los tratamientos y los activos implicados, por lo que partirá de la información
                reflejada en el presente registro de actividades de tratamiento.</p>
                <p>Por otra parte, la necesidad de la llevanza del registro de las actividades del tratamiento afecta a COLEGIO
                PROFESIONAL DE LOGOPEDAS DE CANTABRIA en su calidad de:</p>
                <ul class="mb-3">
                    <li>Responsable del tratamiento, respecto a los tratamientos propios (art. 30.1 RGPD)</li>
                    <li>Encargado del tratamiento, respecto a los servicios prestados a clientes (art. 30.2 RGPD)</li>
                </ul>
                <p>Los registros de las actividades del tratamiento se mantendrán recogidos en el presente documento y
                deberán ser actualizados cuando se realicen cambios o se añadan tratamientos no registrados en
                COLEGIO PROFESIONAL DE LOGOPEDAS DE CANTABRIA . Para ello se deberán establecer revisiones
                periódicas cada año y el responsable interno del tratamiento deberá prestar atención a los cambios
                organizativos y técnicos que alteren el contenido de los registros reflejados en el presente documento,
                debiendo solicitar la modificación del presente documento tan pronto como detecte variaciones en los tratamientos inventariados, Con carácter extraordinario deberá actualizarse el documento cuando se pretendan iniciar nuevos tratamientos.</p>
                <p>Este registro es de uso interno y estará a disposición de la Autoridad de Control (AEPD) cuando sea requerido, tal y como establece el art. 30.4 RGPD.</p>
            </div>
            

