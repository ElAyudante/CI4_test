            <li class="fw-bold fs-3">
                <h3 class="text-uppercase fw-bold">Medidas de seguridad aplicadas en los tratamientos de este registro</h3>
                <div class="fw-normal fs-6">
                    <p>
                        - funciones y obligaciones de los usuarios definidas y documentadas</br>
                        - trabajo fuera de los locales (o acceso remoto) sujeto a autorización</br>
                        - designación de las personas con funciones de control de la seguridad</br>
                        - procedimiento de notificación, gestión y registro de incidentes de seguridad</br>
                        - procedimiento de gestión de soportes incluyendo su identificación, inventario</br>
                        - desecho de soportes con borrado irreversible o destrucción</br>
                        - control de acceso en base a criterio de mínimo necesario</br>
                        - identificación y autenticación inequívoca de los usuarios</br>
                        - renovación periódica de las credenciales de acceso</br>
                        - cifrado de contraseñas</br>
                        - copias de seguridad
                    </p>
                    <p>
                        <b>Medidas de seguridad adicionales para tratamientos con datos de   infracciones adminitrativas, solvencia patrimonial / crédito, datos financieros, datos de medios de pago, perfiles de comportamiento de las personas o de los consumidores</b></br>
                        -- auditorías de seguridad de la información, al menos cada 2 años</br>
                        -- registro de entrada/salida de los soportes con información sensible</br>
                        -- bloqueo de reintentos de autenticación fallidos</br>
                        -- control de acceso físico a los servidores
                    </p>
                    <p>
                        <b>Medidas de seguridad adicionales para tratamientos que involucren datos de categorías sensibles:</b></br>
                        -- auditorías de seguridad de la información, al menos cada 2 años</br>
                        -- registro de entrada/salida de los soportes con información sensible</br>
                        -- bloqueo de reintentos de autenticación fallidos</br>
                        -- control de acceso físico a los servidores</br>
                        --- cifrado de los soportes que almacenan información sensible</br>
                        --- cifrado de redes inalámbricas</br>
                        --- cifrado de la transferencia de datos</br>
                        --- log/registro de accesos a los ficheros con datos sensibles
                    </p>
                    <p>
                        <b>Medidas de seguridad en relación a la documentación en soporte papel:</b></br>
                        - custodia de los documentos durante su tramitación</br>
                        - archivo de documentos bajo llave</br>
                        - criterios de archivo/organización de los documentos para garantizar su localización</br>
                        - destrucción de documentos mediante trituradora o servicios de destrucción confidencial certificada</br>
                        - transporte de los documentos mediante contenedores que garanticen que no pueden ser manipulados</br>
                        además, para los documentos con información de categorías especialmente sensibles:</br>
                        --- los dispositivos de archivo de los documentos se ubicarán en zonas de acceso restringido</br>
                        --- realización de copias de los documentos (papel) con información sensible bajo régimen de autorización</br>
                        --- se limitará las personas que pueden utilizarlos y se dejará constancia de cualquier acceso realizado por otras personas</br>
                        --- verificación periódica de las medidas de seguridad (al menos cada dos años)</br>
                        El listado completo de medidas se encuentra desarrollado en el documento de análisis de riesgos y en el documento de seguridad. Véase también el procedimiento de actuación ante brechas de seguridad.
                    </p>
                </div>
